/** 
*OPERADORES ARITMÉTICOS
* + -> Adição ou concatenação
* - -> Subtração
* * -> Multiplicação
* / -> Divisão
* % -> Resto da divisão
* ** -> Ponenciação
*/

let num1 = 2
let num2 = 5
let resultado

resultado = num1 + num2 // resultado = 7 -> Adição
resultado = num1 + " olá" // resultado = 2 olá -> Concatenação
resultado = num2 + num1 // resultado = 3 -> Subtração
resultado = num2 * num1 // resultado = 10 -> Multiplicação
resultado = num2 / num1 // resultado = 2.5 -> Divisão
resultado = num2 % num1 // resultado = 1 -> Resto da divisão
resultado = num2 ** num1 // resultado = 25 -> Ponenciação

/**
 * Ordem de Precedência
 * ()
 * **
 * * / %
 * + -
 */

